    private void jBtCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtCadastrarActionPerformed
        String usuario = jTextUsuario.getText();
        String nome = jTextNome.getText();
        String email = jTextEmail.getText();
        String senha = jTextSenha.getText();

        if (usuario == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo de Usuário!");
        }
        if (nome == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo de Nome!");
        }
        if (email == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo de E-mail!");
        }
        if (senha == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo de Senha!");
        }

        if (usuarioEmail(usuario, email)) {
            JOptionPane.showMessageDialog(rootPane, "Usuário ou e-mail já cadastrados.");
            return;
        }

        cadastrar(usuario, nome, email, senha);
    }//GEN-LAST:event_jBtCadastrarActionPerformed

