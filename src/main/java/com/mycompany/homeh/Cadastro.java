    private static boolean usuarioEmail(String usuario, String email) {
        Connection conn = Database.getConnection();
        boolean existe = false;

        try {
            PreparedStatement stmt = conn.prepareStatement("SELECT COUNT(*) FROM usuarios WHERE usuario = ? OR email = ?");
            stmt.setString(1, usuario);
            stmt.setString(2, email);

            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                existe = rs.getInt(1) > 0;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return existe;
    }

    private static void cadastrar(String usuario, String nome, String email, String senha) {
        if (inserirUsuario(usuario, nome, email, senha)) {
            JOptionPane.showMessageDialog(null, "Usuário bagulhado!");
        } else {
            JOptionPane.showMessageDialog(null, "Não bagulho o usuário.");
        }
    }

    static boolean inserirUsuario(String usuario, String nome, String email, String senha) {
        Connection conn = Database.getConnection();
        boolean sucesso = false;

        try {
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO usuarios (usuario, nome, email, senha) VALUES (?, ?, ?, ?)");
            stmt.setString(1, usuario);
            stmt.setString(2, nome);
            stmt.setString(3, email);
            stmt.setString(4, senha);

            sucesso = stmt.executeUpdate() > 0;

        } catch (SQLException ex) {
            Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return sucesso;
    }

